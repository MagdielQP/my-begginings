//join junta el array con un valor (el q colocamos en el parametro ) en vez de las comas

let arr1 = ['hans', 'oliver', 'pedro'];
let mensaje = arr1.join(', ');

console.log(mensaje);

// otra manera de dividir seria usando el metodo split donde de parametro colocamos el valor desde donde dividiremos, ejemplo los espacios " "

let saludo = "hola mundo desde mi casa";
let dividido = saludo.split(" ");

console.log(dividido);